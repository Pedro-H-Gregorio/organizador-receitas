    private void updateTabelaReceitas(ArrayList<Receita> receitas) {
        DefaultTableModel tabelaReceitas = (DefaultTableModel) tabela.getModel();
        int i;
        for (i = tabelaReceitas.getRowCount() - 1; i >= 0; i--)
            tabelaReceitas.removeRow(i);
        for (i = 0; i < receitas.size(); i++)
            tabelaReceitas.addRow(new Object[] {
                    receitas.get(i).getTitulo(),
                    receitas.get(i).getTipo().getDescricao()
            });
    }

